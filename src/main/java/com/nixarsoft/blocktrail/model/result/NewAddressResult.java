package com.nixarsoft.blocktrail.model.result;

public class NewAddressResult {

	private String path;
	private String address;
	private String redeem_script;

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getRedeem_script() {
		return redeem_script;
	}

	public void setRedeem_script(String redeem_script) {
		this.redeem_script = redeem_script;
	}
}
